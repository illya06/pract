using System;

namespace Pract 
{
    public static class Calc
    {
        public static int Add(int v1, int v2)
        {
            return v1 + v2;
        }

        public static int Subtract(int v1, int v2)
        {
            return v1 - v2;
        }

        public static int Multiply(int v1, int v2)
        {
            return v1 * v2;
        }
    }
}